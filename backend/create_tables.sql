-- Tabela para Usuarios
CREATE TABLE USUARIO (
    ID INTEGER PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    SENHA VARCHAR2(255) NOT NULL
);

-- Tabela para Regioes
CREATE TABLE REGIAO (
    ID INTEGER PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL
